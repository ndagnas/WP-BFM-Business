	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Permet de définit un ou plusieurs fichiers à envoyer à un site distant.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	class UploadFileCollection : List<UploadFile>
		{
		//***********************************************************************************************************************
		/// <summary>
		/// Ajoute un fichier à la liste.
		/// </summary>
		/// <param name="Form">Nom du formulaire associé.</param>
		/// <param name="FileName">Nom du fichier.</param>
		/// <param name="Raw">Contenue du fichier à envoyer.</param>
		//-----------------------------------------------------------------------------------------------------------------------
		public void Add ( string Form, string FileName, byte[] Raw )
			{
			//-------------------------------------------------------------------------------------------------------------------
			this.Add ( new UploadFile ( Form, FileName, Raw ) );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "System.Web"
