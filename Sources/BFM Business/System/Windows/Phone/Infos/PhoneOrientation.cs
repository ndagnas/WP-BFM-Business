	//  #      #   #   ###   #   #  #####          ###   #   #  #  #####  #   #    #    #   #  ##

	//***************************************************************************************************************************
	// Classe PhoneOrientation
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Permet de gérer les différentes orientations de l'écran.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public enum PhoneOrientation
		{
		/// <summary>
		/// Portrait.
		/// </summary>
		Portrait,
		/// <summary>
		/// Paysage, appbar à droite.
		/// </summary>
		LandscapeLeft,
		/// <summary>
		/// Paysage, appbar à gauche.
		/// </summary>
		LandscapeRight
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************
	
	//  #   #  #####  #  #       ####
	//  #   #    #    #  #      #    
	//  #   #    #    #  #       ### 
	//  #   #    #    #  #          #
	//   ###     #    #  #####  #### 

	//***************************************************************************************************************************
	// Classe PhoneOrientationUtils
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Fournit des méthodes utilisées pour manipuler les chaines.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public static class PhoneOrientationUtils
		{
		//***********************************************************************************************************************
		/// <summary>
		/// Convertit la valeur <b>PageOrientation</b> en valeur <b>PhoneOrientation</b>.
		/// </summary>
		/// <param name="Value">Valeur <b>PageOrientation</b>.</param>
		/// <returns>Valeur <b>PhoneOrientation</b>.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public static PhoneOrientation ToPhoneOrientation ( this PageOrientation Value )
			{
			//-------------------------------------------------------------------------------------------------------------------
			switch ( Value )
				{
				//---------------------------------------------------------------------------------------------------------------
				case PageOrientation.None           : return PhoneOrientation.Portrait;
				case PageOrientation.Portrait       : return PhoneOrientation.Portrait;
				case PageOrientation.Landscape      : return PhoneOrientation.LandscapeLeft;
				case PageOrientation.PortraitUp     : return PhoneOrientation.Portrait;
				case PageOrientation.PortraitDown   : return PhoneOrientation.Portrait;
				case PageOrientation.LandscapeLeft  : return PhoneOrientation.LandscapeLeft;
				case PageOrientation.LandscapeRight : return PhoneOrientation.LandscapeRight;
				//---------------------------------------------------------------------------------------------------------------
				}
			//-------------------------------------------------------------------------------------------------------------------
			
			//-------------------------------------------------------------------------------------------------------------------
			return PhoneOrientation.Portrait;
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************
	
	} // Fin du namespace "System.Windows.Phone.Infos"
