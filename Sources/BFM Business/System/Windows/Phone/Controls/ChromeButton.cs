	//   ###   #   #  #   #   ###   #   #  #####         ####    ###     #      #     ###   #   #

	//***************************************************************************************************************************
	// Classe ChromeButton
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Fournit un bouton de type bar d'outils.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public class ChromeButton : System.Windows.Controls.Button
		{
		//-----------------------------------------------------------------------------------------------------------------------
		// Section des Attributs
		//-----------------------------------------------------------------------------------------------------------------------
		public static readonly DependencyProperty CornerRadiusProperty;
		public static readonly DependencyProperty SourceProperty;
		//-----------------------------------------------------------------------------------------------------------------------
		
		//***********************************************************************************************************************
		#region // Section des Constructeurs
		//-----------------------------------------------------------------------------------------------------------------------
		
		//***********************************************************************************************************************
		/// <summary>
		/// Constructeur statique de l'objet <b>ChromeButton</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		static ChromeButton ()
			{
			//-------------------------------------------------------------------------------------------------------------------
			CornerRadiusProperty = DependencyProperty.Register ( "CornerRadius", typeof (CornerRadius), typeof (ChromeButton), 
				                                 new PropertyMetadata ( new CornerRadius ( 0 ) ) );
			SourceProperty       = DependencyProperty.Register ( "Source"      , typeof (ImageSource ), typeof (ChromeButton), 
				                                 new PropertyMetadata ( null                   ) );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>ChromeButton</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public ChromeButton ()
			{
			//-------------------------------------------------------------------------------------------------------------------
			base.DefaultStyleKey = typeof (ChromeButton);

			base.SetValue ( Microsoft.Phone.Controls.TiltEffect.SuppressTiltProperty, true );

			switch ( DeviceInfos.ScaleFactor )
				{
				//---------------------------------------------------------------------------------------------------------------
				case Factor.Scale_WVGA  : 
				case Factor.Scale_720P  : this.Height  = 56;
				                          this.Width   = 56; 
				                          this.Padding = new Thickness ( 2 ); break;
				//---------------------------------------------------------------------------------------------------------------
				case Factor.Scale_1080P : this.Height  = 56;
				                          this.Width   = 56; 
				                          this.Padding = new Thickness ( 4 ); break;
				//---------------------------------------------------------------------------------------------------------------
				case Factor.Scale_WXGA  : this.Height  = 60;
				                          this.Width   = 60; 
				                          this.Padding = new Thickness ( 5 ); break;
				//---------------------------------------------------------------------------------------------------------------
				}
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************
		
		//-----------------------------------------------------------------------------------------------------------------------
		#endregion
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Obtient ou définit une valeur qui représente le degré auquel les coins d'un 
		/// Border sont arrondis.
		/// </summary>
		/// <returns>Objet <b>CornerRadius</b>.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public CornerRadius CornerRadius
			{
			//-------------------------------------------------------------------------------------------------------------------
			get { return (CornerRadius)base.GetValue ( CornerRadiusProperty ); }
			//-------------------------------------------------------------------------------------------------------------------
			set { base.SetValue ( CornerRadiusProperty, value ); }
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtient ou définit le <b>ImageSource</b> de l'image. 
		/// </summary>
		/// <returns>URI du fichier image.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public ImageSource Source
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return (ImageSource)base.GetValue ( SourceProperty ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set { base.SetValue ( SourceProperty, value ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "System.Windows.Phone.Controls"
