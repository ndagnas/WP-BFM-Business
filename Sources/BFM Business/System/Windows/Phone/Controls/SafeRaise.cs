	//***************************************************************************************************************************
	// Classe SafeRaise
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Implémentation de la gestion des mouvements.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	internal static class SafeRaise
		{
		//***********************************************************************************************************************
		/// <summary>
		/// 
		/// </summary>
		/// <param name="EventToRaise"></param>
		/// <param name="Sender"></param>
		//-----------------------------------------------------------------------------------------------------------------------
		public static void Raise ( EventHandler<EventArgs> EventToRaise, object Sender )
			{
			//-------------------------------------------------------------------------------------------------------------------
			Raise<EventArgs> ( EventToRaise, Sender, EventArgs.Empty );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// 
		/// </summary>
		/// <typeparam name="T"></typeparam>
		/// <param name="EventToRaise"></param>
		/// <param name="Sender"></param>
		/// <param name="Args"></param>
		//-----------------------------------------------------------------------------------------------------------------------
		public static void Raise<T> ( EventHandler<T> EventToRaise, object Sender, T Args ) where T: EventArgs
			{
			//-------------------------------------------------------------------------------------------------------------------
			if ( EventToRaise != null ) EventToRaise ( Sender, Args );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// 
		/// </summary>
		/// <typeparam name="T"></typeparam>
		/// <param name="EventToRaise"></param>
		/// <param name="Sender"></param>
		/// <param name="GetEventArgs"></param>
		//-----------------------------------------------------------------------------------------------------------------------
		public static void Raise<T> ( EventHandler<T> EventToRaise, object Sender, 
		                                          GetEventArgs<T> GetEventArgs ) where T: EventArgs
			{
			//-------------------------------------------------------------------------------------------------------------------
			if ( EventToRaise != null ) EventToRaise ( Sender, GetEventArgs () );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// 
		/// </summary>
		/// <typeparam name="T"></typeparam>
		/// <returns></returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public delegate T GetEventArgs<T> () where T: EventArgs;
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "System.Windows.Phone.Controls"
