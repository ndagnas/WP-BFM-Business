		/// <summary>
		/// Initializes a new instance of the DismissingEventArgs class.
		/// </summary>
		/// <param name="result">The result value.</param>
		//-----------------------------------------------------------------------------------------------------------------------
		public DismissingEventArgs ( CustomMessageBoxResult Result ) { this.Result = Result; }
