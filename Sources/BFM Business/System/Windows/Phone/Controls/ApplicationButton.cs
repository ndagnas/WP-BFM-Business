	//  #   #  #      #             ####    ###     #      #     ###   #   #

	//***************************************************************************************************************************
	// Classe ApplicationButton
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Fournit un bouton de type bar d'outils.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public class ApplicationButton : System.Windows.Controls.Button
		{
		//-----------------------------------------------------------------------------------------------------------------------
		// Section des Attributs
		//-----------------------------------------------------------------------------------------------------------------------
		public static readonly DependencyProperty InnerHeightProperty;
		public static readonly DependencyProperty InnerWidthProperty;
		public static readonly DependencyProperty SourceProperty;
		public static readonly DependencyProperty ThicknessProperty;
		//-----------------------------------------------------------------------------------------------------------------------
		
		//***********************************************************************************************************************
		#region // Section des Constructeurs
		//-----------------------------------------------------------------------------------------------------------------------
		
		//***********************************************************************************************************************
		/// <summary>
		/// Constructeur statique de l'objet <b>ApplicationButton</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		static ApplicationButton ()
			{
			//-------------------------------------------------------------------------------------------------------------------
			InnerHeightProperty = DependencyProperty.Register ( "InnerHeight", typeof (double     ), typeof (ApplicationButton), 
				                                           new PropertyMetadata ( (double)72.0 ) );
			//-------------------------------------------------------------------------------------------------------------------
			InnerWidthProperty  = DependencyProperty.Register ( "InnerWidth" , typeof (double     ), typeof (ApplicationButton), 
				                                           new PropertyMetadata ( (double)72.0 ) );
			//-------------------------------------------------------------------------------------------------------------------
			SourceProperty      = DependencyProperty.Register ( "Source"     , typeof (ImageSource), typeof (ApplicationButton), 
				                                           new PropertyMetadata ( null         ) );
			//-------------------------------------------------------------------------------------------------------------------
			ThicknessProperty   = DependencyProperty.Register ( "Thickness"  , typeof (double     ), typeof (ApplicationButton), 
				                                           new PropertyMetadata ( (double)4.0  ) );
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>ApplicationButton</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public ApplicationButton ()
			{
			//-------------------------------------------------------------------------------------------------------------------
			base.DefaultStyleKey = typeof (ApplicationButton);

			base.SetValue ( Microsoft.Phone.Controls.TiltEffect.IsTiltEnabledProperty, true );

			this.Height = DeviceInfos.ApplicationBarHeight;
			this.Width  = DeviceInfos.ApplicationBarHeight; 
			//-------------------------------------------------------------------------------------------------------------------

			//-------------------------------------------------------------------------------------------------------------------
			if ( DeviceInfos.DeviceType == DeviceType.SmartPhone )
				{
				//---------------------------------------------------------------------------------------------------------------
				base.SetValue ( InnerHeightProperty, 48.0 );
				base.SetValue ( InnerWidthProperty , 48.0 ); 
				base.SetValue ( ThicknessProperty  ,  3.5 );
				//---------------------------------------------------------------------------------------------------------------
				}
			else
				{
				//---------------------------------------------------------------------------------------------------------------
				base.SetValue ( InnerHeightProperty, 35.0 );
				base.SetValue ( InnerWidthProperty , 35.0 ); 
				base.SetValue ( ThicknessProperty  ,  2.5 );
				//---------------------------------------------------------------------------------------------------------------
				}
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************
		
		//-----------------------------------------------------------------------------------------------------------------------
		#endregion
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtient ou définit la hauteur du bouton.
		/// </summary>
		/// <returns>Hauteur du bouton.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public double InnerHeight
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return (double)base.GetValue ( InnerHeightProperty        ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set {                base.SetValue ( InnerHeightProperty, value ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtient ou définit la largeur du bouton.
		/// </summary>
		/// <returns>Largeur du bouton.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public double InnerWidth
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return (double)base.GetValue ( InnerWidthProperty        ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set {                base.SetValue ( InnerWidthProperty, value ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtient ou définit le <b>ImageSource</b> de l'image. 
		/// </summary>
		/// <returns>URI du fichier image.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public ImageSource Source
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return (ImageSource)base.GetValue ( SourceProperty        ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set {                     base.SetValue ( SourceProperty, value ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtient ou définit l'épaisseur du cercle.
		/// </summary>
		/// <returns>Epaisseur du cercle.</returns>
		//-----------------------------------------------------------------------------------------------------------------------
		public double Thickness
			{
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			get { return (double)base.GetValue ( ThicknessProperty        ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			set {                base.SetValue ( ThicknessProperty, value ); }
			//~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~~
			}
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "System.Windows.Phone.Controls"
