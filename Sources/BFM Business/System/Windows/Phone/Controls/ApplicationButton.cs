namespace System.Windows.Phone.Controls
	{

	//   ###   ####   ####          ####   #   #  #####  #####   ###   #   #
	//  #   #  #   #  #   #         #   #  #   #    #      #    #   #  ##  #
	//  #####  ####   ####   #####  ####   #   #    #      #    #   #  # # #
