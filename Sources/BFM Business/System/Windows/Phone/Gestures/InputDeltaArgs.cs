	//***************************************************************************************************************************
	// Classe InputDeltaArgs
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Implémentation de la gestion des mouvements.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	internal abstract class InputDeltaArgs : InputBaseArgs
		{
		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>InputDeltaArgs</b>.
		/// </summary>
		/// <param name="Source">Origine de l'appel.</param>
		/// <param name="Origin">Point d'origine du mouvement.</param>
		//-----------------------------------------------------------------------------------------------------------------------
		protected InputDeltaArgs ( UIElement Source, Point Origin ) : base ( Source, Origin ) {}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens la valeur cumulée.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public abstract Point CumulativeTranslation { get; }
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens la valeur courante.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public abstract Point DeltaTranslation { get; }
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "System.Windows.Phone.Gestures"
