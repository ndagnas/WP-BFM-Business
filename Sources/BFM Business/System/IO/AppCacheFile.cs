	//***************************************************************************************************************************
	// Classe AppCacheFile
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Représente un fichier contenue dans le cache.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public class AppCacheFile
		{
		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens ou définit le contenue du fichier.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public string Content { get; set; }
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens ou définit le nom du fichier.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public string Name { get; set; }
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens ou définit la date du fichier.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public DateTime Date { get; set; }
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Indique si l'objet est initialisé ou non.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public bool IsEmpty
			{
			//-------------------------------------------------------------------------------------------------------------------
			get
				{
				//---------------------------------------------------------------------------------------------------------------
				return string.IsNullOrEmpty ( this.Content ) && string.IsNullOrEmpty ( this.Name );
				//---------------------------------------------------------------------------------------------------------------
				}
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens un objet non initialisé.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public static AppCacheFile Empty { get { return new AppCacheFile (); } }
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "System.IO"
