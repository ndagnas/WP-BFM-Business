	//***************************************************************************************************************************
	// Classe ObjectEventArgs
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Définit un argument de délégué du type spécifié.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public sealed class ObjectEventArgs<T> : EventArgs
		{
		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>ObjectEventArgs</b>.
		/// </summary>
		/// <param name="Sender">Objet à passer.</param>
		//-----------------------------------------------------------------------------------------------------------------------
		public ObjectEventArgs ( T Owner ) { this.Self = Owner; }
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Obtiens l'objet liés à l'événement.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public T Self { get; private set; }
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "System"
