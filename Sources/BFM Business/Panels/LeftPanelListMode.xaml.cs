	//***************************************************************************************************************************
	// Contrôle LeftPanelListMode
	//***************************************************************************************************************************
	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Définit la page permettant l'identification.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public partial class LeftPanelListMode : UserControl
		{
		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>LeftPanelListMode</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public LeftPanelListMode ()
			{
			//-------------------------------------------------------------------------------------------------------------------
			this.InitializeComponent ();
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Est appelé lors d'un clic sur un lien interne.
		/// </summary>
		/// <param name="Sender">Objet source de l'appel.</param>
		/// <param name="Args"><b>RoutedEventArgs</b> qui contient les données d'événement.</param>
		//-----------------------------------------------------------------------------------------------------------------------
		private void OnSectionClick ( object Sender, RoutedEventArgs Args )
			{
			//-------------------------------------------------------------------------------------------------------------------
			FrameworkElement Self = Sender as FrameworkElement;

			if ( Self != null && this.Click != null )
				{
				//---------------------------------------------------------------------------------------------------------------
				SectionType Section = SectionType.All;
				//---------------------------------------------------------------------------------------------------------------
				
				//---------------------------------------------------------------------------------------------------------------
				switch ( Self.Tag.ToString () )
					{
					//-----------------------------------------------------------------------------------------------------------
					case "SectionType.France"       : Section = SectionType.France;       break;
					case "SectionType.World"        : Section = SectionType.World;        break;
					case "SectionType.Employment"   : Section = SectionType.Employment;   break;
					case "SectionType.YourMoney"    : Section = SectionType.YourMoney;    break;
					case "SectionType.Innovation"   : Section = SectionType.Innovation;   break;
					case "SectionType.Construction" : Section = SectionType.Construction; break;
					case "SectionType.Banking"      : Section = SectionType.Banking;      break;
					case "SectionType.Culture"      : Section = SectionType.Culture;      break;
					case "SectionType.Sport"        : Section = SectionType.Sport;        break;
					case "SectionType.Energy"       : Section = SectionType.Energy;       break;
					case "SectionType.Distribution" : Section = SectionType.Distribution; break;
					case "SectionType.Transport"    : Section = SectionType.Transport;    break;
					case "SectionType.Tourism"      : Section = SectionType.Tourism;      break;
					case "SectionType.Industry"     : Section = SectionType.Industry;     break;
					case "SectionType.Observatory"  : Section = SectionType.Observatory;  break;
					//-----------------------------------------------------------------------------------------------------------
					case "Options" : { this.Click ( this, EventArgs.Empty ); return; }
					//-----------------------------------------------------------------------------------------------------------
					}
				//---------------------------------------------------------------------------------------------------------------
				
				//---------------------------------------------------------------------------------------------------------------
				this.Click ( this, new ObjectEventArgs<SectionType> ( Section ) );
				//---------------------------------------------------------------------------------------------------------------
				}
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************
		
		//***********************************************************************************************************************
		/// <summary>
		/// Se produit lors d'un clic sur un élément de la page.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public event EventHandler Click;
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
	//***************************************************************************************************************************

	} // Fin du namespace "NextRadio.Panels"
