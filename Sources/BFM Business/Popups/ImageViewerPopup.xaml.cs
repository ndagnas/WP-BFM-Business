	#region // Déclaration et Implémentation de l'Objet
	//---------------------------------------------------------------------------------------------------------------------------
	/// <summary>
	/// Fournit une fenêtre de zoom sur une photo.
	/// </summary>
	//---------------------------------------------------------------------------------------------------------------------------
	public partial class ImageViewerPopup : UserControl
		{
		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>ImageViewerPopup</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public ImageViewerPopup () { this.InitializeComponent (); }
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Initialise une nouvelle instance de l'objet <b>ImageViewerPopup</b>.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public ImageViewerPopup ( Uri Source ) : this ()
			{
			//-------------------------------------------------------------------------------------------------------------------
			this.ImageViewer.Source = new BitmapImage ( Source );

			this.ImageUrl.Text = Source.OriginalString;
			//-------------------------------------------------------------------------------------------------------------------
			}
		//***********************************************************************************************************************

		//***********************************************************************************************************************
		/// <summary>
		/// Libère les ressources.
		/// </summary>
		//-----------------------------------------------------------------------------------------------------------------------
		public void Close () { try { this.ImageViewer.Source = null; } catch {} }
		//***********************************************************************************************************************
		}
	//---------------------------------------------------------------------------------------------------------------------------
	#endregion
